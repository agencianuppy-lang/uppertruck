  <script>
    (function () {
      // Frase a ser "digitada" em loop
      const TEXT = "Algo muito grande está pro vir.";

      // Velocidades e tempos (em ms) — ajuste se quiser
      const TYPING_SPEED = 70;      // digitação
      const DELETING_SPEED = 45;    // apagar
      const HOLD_AFTER_TYPE = 1200; // pausa quando termina de digitar
      const HOLD_AFTER_DELETE = 400;// pausa quando termina de apagar
      const CARET_BLINK = 450;      // piscada do cursor

      const el = document.getElementById("typing-text");
      const caret = document.getElementById("typing-caret");

      let i = 0;
      let direction = 1; // 1 = digitando, -1 = apagando

      function step() {
        if (direction === 1) {
          // Digitando
          if (i < TEXT.length) {
            el.textContent += TEXT.charAt(i);
            i++;
            setTimeout(step, TYPING_SPEED);
          } else {
            // terminou de digitar → pausa → começa a apagar
            setTimeout(() => { direction = -1; step(); }, HOLD_AFTER_TYPE);
          }
        } else {
          // Apagando
          if (i > 0) {
            i--;
            el.textContent = TEXT.substring(0, i);
            setTimeout(step, DELETING_SPEED);
          } else {
            // terminou de apagar → pausa → recomeça a digitar
            setTimeout(() => { direction = 1; step(); }, HOLD_AFTER_DELETE);
          }
        }
      }

      // Cursor piscando sem CSS extra
      setInterval(() => {
        if (caret) caret.style.opacity = (caret.style.opacity === "0" ? "1" : "0");
      }, CARET_BLINK);

      // Inicia
      el.textContent = "";
      step();
    })();
  </script>


